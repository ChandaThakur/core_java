package day2.Methods;

public class Example1 {
	
	
		
		public static void add() {
			int x=4;
			int y =7;
			int z=x+y;
			
			System.out.println(z);
		}
		
		public static void sub() {
			int x=4;
			int y =7;
			int z=x-y;
			
			System.out.println(z);
			
		}
		public static void main(String[] args) {
			add();
			sub();
		}

}
